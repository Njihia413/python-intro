#Tuples are like lists
#Difference is that tuples are immutable i.e Their values cannot be changed once created
#Tuples can be created using parentheses() or using the tuple() function
tuple_a = ("a","b","c","d") #tuple of strings
tuple_b = (1,2,3,4,5,6) #tuple of numbers
tuple_c = (1,"west",34,"longitude") #mixed tuple
tuple_d = tuple() #empty tuple

print(tuple_a)
print(tuple_b)
print(tuple_c)
print(tuple_d)